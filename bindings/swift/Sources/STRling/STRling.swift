/// STRling - A next-generation production-grade syntax for writing powerful regular expressions
///
/// STRling is designed as a user interface for writing powerful regular expressions (RegEx)
/// with an object-oriented approach and instructional error handling. STRling recognizes
/// the cryptic nature of raw regular expression is challenging and susceptible to errors,
/// which is why STRling keeps it as far from you as possible while maintaining the same
/// power and flexibility.
///
/// This is the main public API for the STRling Swift binding. The core implementation
/// details are kept internal to maintain a clean public interface.
public struct STRling {
    /// The version of the STRling library
    public static let version = "3.0.0-alpha"
    
    /// Private initializer to prevent instantiation
    private init() {}
}
